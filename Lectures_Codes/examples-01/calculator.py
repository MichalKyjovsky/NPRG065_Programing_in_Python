#!/usr/bin/env python3

# We can use Python as a simple calculator - ideally in the interactive shell.
# Let us ignore the actual types of the values for now (we will return to it later).

1 + 2

2 + 2 * 3    # priority is as usual

(2 + 2) * 3  # but we can change it (as usual)

2 / 3        # common division

2 // 3       # division in integer

2 % 3        # modulo

2 ** 3       # 2 power 3

2 ** 1000    # big numbers are no issue

1.2 + 1e2    # float/point values work too

